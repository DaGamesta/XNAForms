namespace XNAForms
{
    /// <summary>
    /// Denotes a cursor type.
    /// </summary>
    public enum CursorType
    {
        /// <summary>
        /// Denotes a normal cursor.
        /// </summary>
        NORMAL,
        /// <summary>
        /// Denotes a vertically resize cursor.
        /// </summary>
        RESIZE_VERTICAL,
        /// <summary>
        /// Denotes a horizontally resize cursor.
        /// </summary>
        RESIZE_HORIZONTAL,
        /// <summary>
        /// Denotes a diagonally resize cursor.
        /// </summary>
        RESIZE_DIAGONAL,
        /// <summary>
        /// Denotes a text cursor.
        /// </summary>
        TEXT
    }
}
