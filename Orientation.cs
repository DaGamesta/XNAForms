namespace XNAForms
{
    /// <summary>
    /// Indicates the orientation of an object.
    /// </summary>
    public enum Orientation : byte
    {
        /// <summary>
        /// Indicates no specific orientation.
        /// </summary>
        NONE = 0,
        /// <summary>
        /// Indicates horizontal orientation.
        /// </summary>
        HORIZONTAL,
        /// <summary>
        /// Indicates vertical orientation.
        /// </summary>
        VERTICAL
    }
}
