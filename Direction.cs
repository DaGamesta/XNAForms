namespace XNAForms
{
    /// <summary>
    /// Represents the four ordinal directions.
    /// </summary>
    public enum Direction : byte
    {
        /// <summary>
        /// Represents no direction.
        /// </summary>
        NONE = 0,
        /// <summary>
        /// Represents left.
        /// </summary>
        LEFT = 1,
        /// <summary>
        /// Represents up.
        /// </summary>
        UP = 2,
        /// <summary>
        /// Represents right.
        /// </summary>
        RIGHT = 4,
        /// <summary>
        /// Represents down.
        /// </summary>
        DOWN = 8
    }
}
